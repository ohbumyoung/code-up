package com.kh.ex1;

public class Quiz1 {

	public static void main(String[] args) {
		
		method1();
		
	}
	
		
		public static void method1() {
			
			int n1 = 10;
			
			int n2 = 3;
			
			// n1, n2의 합, 차, 곱, 몫, 나머지 출력
			
			System.out.println("n1 + n2= " + (n1 + n2));
				
			System.out.println("n1 - n2= " + (n1 - n2));
			
			System.out.println("n1 * n2= " + (n1 * n2));
			
			System.out.println("n1 % n2= " + (n1 % n2));
			
			System.out.println("n1 / n2= " + (n1 / n2));
			
			
			
		
		}
}


